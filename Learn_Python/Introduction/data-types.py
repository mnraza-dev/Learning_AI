"""
Numeric Type
int - whole numbers 100 200 -100
float - decimal numbers 1.124 -10.9
complex - real & imaginary part
eg: a+bj 3+4j

"""

a = 10   #int number
b = 10.2 #float number
c = 3+4j #complex number
print(a)
print(b)
print(c)

isRaining = True #boolean
result = None #NoneType
print(isRaining)
print(result)

text = "Hello World" #string 'Hello World'
print(text)


# List - ordered, mutable, allows duplicates
my_list = [1,2,3,4,5] #list [1,2,3,4,5]
print(my_list)
print(type(my_list))

# Tuple - ordered, immutable, allows duplicates
my_tuple = (1,2,3,4,5) #tuple (1,2,3,4,5)
print(my_tuple)
print(type(my_tuple))









