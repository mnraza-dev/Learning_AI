str = 'Python is kind of easy'

print(str.lower()) #Convert to lowercase
print(str.upper()) #Convert to uppercase
print(str.title()) #Convert to title case
print(str.capitalize()) #Convert to capitalize
print(str.swapcase()) #Swap case
print(str.casefold()) #Convert to lowercase
print(str.islower()) #Check if all characters are lowercase
print(str.isupper()) #Check if all characters are uppercase
print(str.istitle()) #Check if all words are capitalized