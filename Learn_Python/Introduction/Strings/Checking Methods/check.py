str = 'Python programming'
print(str.startswith('Python')) #Check if the string starts with the given substring

print(str.endswith('programming')) #Check if the string ends with the given substring
print(str.isalpha()) #Check if the string is alphabetic
print(str.isdigit()) #Check if the string is numeric
print(str.isalnum()) #Check if the string is alphanumeric
print(str.isspace()) #Check if the string is a space
print(str.isprintable()) #Check if the string is printable
print(str.isidentifier()) #Check if the string is a valid identifier